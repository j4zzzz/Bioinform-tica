def obtener_k_mers_frecuentes(secuencias, k):
    conteo_k_mers = {}
    for secuencia in secuencias:
        k_mers_vistos = set()
        for i in range(len(secuencia) - k + 1):
            k_mer = secuencia[i:i+k]
            k_mers_vistos.add(k_mer)
        for k_mer in k_mers_vistos:
            conteo_k_mers[k_mer] = conteo_k_mers.get(k_mer, 0) + 1

    max_frecuencia = max(conteo_k_mers.values())
    k_mers_mas_frecuentes = {k_mer: frec for k_mer, frec in conteo_k_mers.items() if frec == max_frecuencia}
    return k_mers_mas_frecuentes

