    nucleotido_frecuente = ""
    for nucleotido in nucleotidos:
        frecuencia_actual = matriz_frecuencias[nucleotido][posicion]
        if frecuencia_actual > max_frecuencia:
            max_frecuencia = frecuencia_actual
